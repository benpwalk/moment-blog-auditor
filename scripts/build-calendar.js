// Regenerate data/calendar.json from your Google Sheet.
//
// USAGE (CSV files you downloaded from Sheets — File > Download > CSV per tab):
//   node scripts/build-calendar.js athlete=./athlete.csv entrepreneur=./entrepreneur.csv
//
// USAGE (published-to-web CSV URLs — no API, auto-current each time you run it):
//   node scripts/build-calendar.js athlete="https://docs.google.com/.../pub?gid=0&output=csv" entrepreneur="https://.../pub?gid=123&output=csv"
//
// You can also set CALENDAR_CSV_ATHLETE / CALENDAR_CSV_ENTREPRENEUR env vars instead of args.
// The pillar list rarely changes, so it lives here (edit if you add/retire a pillar topic).

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { csvToPosts } from "../src/csv.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const PILLARS = {
  athlete: [
    "athlete wealth management",
    "financial planning for professional athletes",
    "estate planning for professional athletes",
    "tax planning for professional athletes",
    "investment management for professional athletes",
    "risk management for athletes",
  ],
  entrepreneur: [
    "wealth management for business owners",
    "financial planning for business owners",
    "estate planning for business owners",
    "tax planning for business owners",
    "investment management for business owners",
    "risk management for business owners",
  ],
};

function argOrEnv(key, envKey) {
  const a = process.argv.find((x) => x.startsWith(key + "="));
  return a ? a.slice(key.length + 1).replace(/^["']|["']$/g, "") : process.env[envKey] || null;
}

async function load(src) {
  if (!src) return "";
  if (/^https?:\/\//i.test(src)) {
    const res = await fetch(src);
    if (!res.ok) throw new Error(`Fetch failed (${res.status}) for ${src}`);
    return await res.text();
  }
  return fs.readFileSync(src, "utf8");
}

const athleteSrc = argOrEnv("athlete", "CALENDAR_CSV_ATHLETE");
const entSrc = argOrEnv("entrepreneur", "CALENDAR_CSV_ENTREPRENEUR");

const posts = [
  ...csvToPosts(await load(athleteSrc), "athlete"),
  ...csvToPosts(await load(entSrc), "entrepreneur"),
];

const calendar = {
  _comment: "Auto-generated by scripts/build-calendar.js from the Google Sheet. Re-run after updating the sheet.",
  generatedAt: new Date().toISOString(),
  domain: "www.momentprivatewealth.com",
  pillars: PILLARS,
  posts,
};

const outPath = path.join(__dirname, "../data/calendar.json");
fs.writeFileSync(outPath, JSON.stringify(calendar, null, 2));
console.log(`Wrote ${posts.length} posts to ${outPath}`);
const withSlug = posts.filter((p) => p.slug).length;
console.log(`  ${withSlug} have published URLs (auditable by URL); ${posts.length - withSlug} are planned/draft-only.`);
